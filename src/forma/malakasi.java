    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String mutaxassisligi = jTextField1.getText();
        String lavozimi = jTextField2.getText();
        String malaka_yli = jTextField4.getText();
        String []item = jComboBox1.getSelectedItem().toString().split(" ");
        Integer id_ishchi = ishchiservice.getPersonIdByInitial(item);
        
        jTextField1.setText(null);
        jTextField2.setText(null);
        jTextField4.setText(null);
        Malaka malaka = new Malaka(mutaxassisligi,lavozimi,malaka_yli,id_ishchi);
        
        if(malakaservice.addMalaka(malaka)){
            JOptionPane.showMessageDialog(this, "Added");
            jTable1.setModel(DbUtils.resultSetToTableModel(malakaservice.getMalaka()));
        }
        else{
            JOptionPane.showMessageDialog(this, "Error");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

