    Integer id_malaka;
    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
               // TODO add your handling code here:
        TableModel model = jTable1.getModel();
        Object valueAt = model.getValueAt(jTable1.getSelectedRow(), 0);
        
        id_malaka = Integer.parseInt(valueAt.toString());
        Malaka malaka_id = malakaservice.getMalakaById(id_malaka);
        
        jTextField1.setText(malaka_id.getMutaxassisligi());
        jTextField2.setText(malaka_id.getLavozimi());
        jTextField4.setText(malaka_id.getMalaka_yli());
    }//GEN-LAST:event_jTable1MouseClicked

