    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        String firstname = jTextField1.getText();
        String lastname = jTextField2.getText();
        String seriya = jTextField4.getText();
        
        Person person = new Person(id_person,firstname,lastname,seriya);
        
        if(service.editPerson(person)){
            JOptionPane.showMessageDialog(Forma.this, "Edited");
            jTable1.setModel(DbUtils.resultSetToTableModel(service.getworkers()));
        }
        else{
            JOptionPane.showMessageDialog(Forma.this, "Error");
        }
        
    }//GEN-LAST:event_jButton3ActionPerformed
    
    private Integer id_person;
    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        TableModel model = jTable1.getModel();
        Object valueAt = model.getValueAt(jTable1.getSelectedRow(), 0);
        
        id_person = Integer.parseInt(valueAt.toString());
        Person personId = service.getPersonById(id_person);
        
        jTextField1.setText(personId.getIsmi());
        jTextField2.setText(personId.getFamiliyasi());
        jTextField4.setText(personId.getSeriya());
    }//GEN-LAST:event_jTable1MouseClicked

