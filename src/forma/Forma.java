    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
        String firstname = jTextField1.getText();
        String lastname = jTextField2.getText();
        String seriya = jTextField4.getText();
        
        jTextField1.setText(null);
        jTextField2.setText(null);
        jTextField4.setText(null);
        Person person = new Person(firstname,lastname,seriya);
        
        if(service.addIshchi(person)){
            JOptionPane.showMessageDialog(Forma.this, "Added");
            jTable1.setModel(DbUtils.resultSetToTableModel(service.getworkers()));
        }
        else{
            JOptionPane.showMessageDialog(Forma.this, "Error");
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

